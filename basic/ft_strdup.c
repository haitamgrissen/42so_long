#include "libft.h"

char	*ft_strdup(const char *s1)
{
	size_t	i;
	size_t	lenght;
	char	*result;

	lenght = ft_strlen(s1);
	result = (char *)malloc(sizeof(char) * (lenght + 1));
	if (result)
	{
		i = 0;
		while (s1[i])
		{
			result[i] = s1[i];
			i++;
		}
		result[i] = '\0';
	}
	return (result);
}
